package net.sf.aislib.tools.mapping.library.structure;

public class JavaParam {
  
  private String name;
  private String type;
  
  public JavaParam(String name, String type) {
    this.name = name;
    this.type = type;
  }
  
  public String getName() {
    return name;
  }
  public String getType() {
    return type;
  }

}

